public class Paquete {
    private int peso;
    private int Valor;

    public Paquete(int peso, int valor) {
        this.peso = peso;
        Valor = valor;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }

    public int getValor() {
        return Valor;
    }

    public void setValor(int valor) {
        Valor = valor;
    }

}